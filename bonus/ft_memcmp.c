int	ft_memcmp(const char *s1, const char *s2, int n)
{
	int	i;

	i = -1;
	if (!s1 || !s2)
		return (0);
	while (++i < n)
		if (s1[i] != s2[i])
			return (0);
	if (s1[n] == '\n')
		return (1);
	return (0);
}
